#include "map.h"

me3WALL ME3_MapWalls[17] =
{
  {-10, 10, 9.4, 9.9},     /*1*/
  {9.7, 10.2, 0, 9.7},     /*2*/
  {9.7, 10.2, -9.8, -3.5}, /*3*/
  {-10, 10, -10, -9.6},   /*4*/
  {-10.2, -9.7, -9.8, 9.7},/*5*/
  {-0.4, 0.4, 6.7, 9.7},   /*6*/
  {3.4, 6.8, 6.4, 7.2},    /*7*/
  {-6.8, -3.4, 6.5, 7.2},  /*8*/
  {6.8, 10, 3.1, 3.7},     /*9*/
  {6.5, 7.2, -3.5, 3.5},   /*10*/
  {6.8, 10, -7.3, -6.4},   /*11*/
  {-3.4, 6.8, -3.9, -3.1}, /*12*/
  {-6.8, 3.4, -7.2, -6.5}, /*13*/
  {3.0, 3.7, -6.7, 6.7},   /*14*/
  {-3.4, 3.4, -0.4, 0.4},  /*15*/
  {-3.7, -3.0, 0, 3.5},    /*16*/
  {-7.1, -6.5, -6.7, 6.7}  /*17*/
};

BOOL Camin1Wall(FLT x, FLT z, me3WALL *w)
{
  if ((x > w->MinX) && (x < w->MaxX) && (z > w->MinZ) && (z < w->MaxZ))
    return TRUE;
  return FALSE;
}

BOOL ME3_CaminWall(FLT x, FLT z)
{
  int i;

  for (i = 0; i < 17; i++)
    if (Camin1Wall(x, z, &ME3_MapWalls[i]))
      return TRUE;

  return FALSE;
}